//Parent class with just one variable 
//This program aims to show the multilevel inheritence
public class FirstLevelClass {
	private int firstLevelClassVariable;

	/**
	 * @param firstLevelClassVariable
	 */
	public FirstLevelClass(int firstLevelClassVariable) {
		this.firstLevelClassVariable = firstLevelClassVariable;
		System.out.println("firstLevelClassVariable = " + firstLevelClassVariable);
	}

}
